#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int	main(int argc, char **argv)
{
	if (argc > 0)
	{
		ft_putstr(*argv);
	}
	write(1, "\n", 1);
	return (0);
}
